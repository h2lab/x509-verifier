#include "sig-verif.h"
#include "../../libecc-eddsa-sm2-newapi/src/libsig.h" /* FIXME use -I */

int x509_sig_verify(const x509_sig_verify_ctx *ctx)
{
	ec_params ecp_secp384r1;
	ec_pub_key pubkey;
	u8 sig[48*2];
	int ret;

	u8 pub_key_buf[] = {
		0x04, 0xd4, 0xbc, 0x3d, 0x02, 0x42, 0x75, 0x41,
		0x13, 0x23, 0xcd, 0x80, 0x04, 0x86, 0x02, 0x51,
		0x2f, 0x6a, 0xa8, 0x81, 0x62, 0x0b, 0x65, 0xcc,
		0xf6, 0xca, 0x9d, 0x1e, 0x6f, 0x4a, 0x66, 0x51,
		0xa2, 0x03, 0xd9, 0x9d, 0x91, 0xfa, 0xb6, 0x16,
		0xb1, 0x8c, 0x6e, 0xde, 0x7c, 0xcd, 0xdb, 0x79,
		0xa6, 0x2f, 0xce, 0xbb, 0xce, 0x71, 0x2f, 0xe5,
		0xa5, 0xab, 0x28, 0xec, 0x63, 0x04, 0x66, 0x99,
		0xf8, 0xfa, 0xf2, 0x93, 0x10, 0x05, 0xe1, 0x81,
		0x28, 0x42, 0xe3, 0xc6, 0x68, 0xf4, 0xe6, 0x1b,
		0x84, 0x60, 0x4a, 0x89, 0xaf, 0xed, 0x79, 0x0f,
		0x3b, 0xce, 0xf1, 0xf6, 0x44, 0xf5, 0x01, 0x78,
		0xc0 };

	if (ctx == NULL) {
		ret = -1;
		goto err;
	}

	import_params(&ecp_secp384r1, &secp384r1_str_params);

	/* Import pub key */
	ret = ec_pub_key_import_from_aff_buf(&pubkey, &ecp_secp384r1,
					     pub_key_buf + 1, sizeof(pub_key_buf) - 1,  /*remove leadnig 0x04 */
					     ECDSA);
	if (ret) {
		goto err;
	}

	/*
	 * For ECDSA, libecc expects a sig in raw format, i.e. concatenated
	 * R and S and not two integers as found in certs
	 */
	local_memcpy(sig, ctx->sig + 7, 48);
	local_memcpy(sig+48, ctx->sig + 58, 48);

	/* verify tbs */
	ret = ec_verify(sig, 96, &pubkey,
			ctx->tbs, ctx->tbs_len,
			ECDSA, SHA384, NULL, 0);

err:
	return ret;
}
